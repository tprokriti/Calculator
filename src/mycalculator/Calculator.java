public void equation(){
    if(store==1)
    {
        result = FirstElement + Double.parseDouble(jTextField1.getText());
        jTextField1.setText(Double.toString(result));
    }
    else if(store==2)
        {
        result = FirstElement - Double.parseDouble(jTextField1.getText());
        jTextField1.setText(Double.toString(result));
    }
    else if(store==3)
        {
        result = FirstElement * Double.parseDouble(jTextField1.getText());
        jTextField1.setText(Double.toString(result));
    }
    else if(store==4)
        {
        result = FirstElement / Double.parseDouble(jTextField1.getText());
        jTextField1.setText(Double.toString(result));
    }
    else if(store==5)
        {
        result = FirstElement / Double.parseDouble(jTextField1.getText())*100;
        jTextField1.setText(Double.toString(result));
    }
}
